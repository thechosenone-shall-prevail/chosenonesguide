export function getThemeById(id: string): ThemeConfig | undefined {
  return SYSTEM_THEMES.find((theme) => theme.id === id);
}

export function getSystemThemes(): ThemeConfig[] {
  return SYSTEM_THEMES.filter((theme) => theme.isSystem);
}

export function getPremiumThemes(): ThemeConfig[] {
  return SYSTEM_THEMES.filter((theme) => theme.isPremium);
}

export function getFreeThemes(): ThemeConfig[] {
  return SYSTEM_THEMES.filter((theme) => !theme.isPremium);
}
